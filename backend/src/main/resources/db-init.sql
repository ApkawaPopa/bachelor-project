create table example
(
    id           integer generated by default as identity primary key,
    owner        varchar,
    text_message varchar
);

create table "user"
(
    id                    integer generated by default as identity primary key,
    username              varchar unique not null,
    password_hash         varchar        not null,
    public_key            varchar        not null,
    encrypted_private_key varchar        not null,
    created_at            timestamp      not null,
    updated_at            timestamp      not null
);

create table chat
(
    id         integer generated by default as identity primary key,
    created_at timestamp not null,
    updated_at timestamp not null
);

create table user_chat
(
    user_id    integer   not null references "user" (id),
    chat_id    integer   not null references chat (id),
    created_at timestamp not null,
    primary key (user_id, chat_id)
);

create table message
(
    id         integer generated by default as identity primary key,
    sender_id  integer references "user" (id) not null,
    chat_id    integer references chat (id)   not null,
    content    varchar                        not null,
    created_at timestamp                      not null,
    updated_at timestamp                      not null
);

create table web_socket_token
(
    id      uuid primary key,
    user_id integer unique references "user" (id) not null
)